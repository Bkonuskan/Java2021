package day03_datacasting_increment;

public class C3_DataCasting {

	public static void main(String[] args) {
		
		byte sayi1 = 10;
		System.out.println("byte değişken ile : " + sayi1);
		short sayi2 = sayi1;
		System.out.println("short değişken ile : " + sayi2);
		int sayi3 = sayi2;
		System.out.println("int değişken ile : " + sayi3);
        float sayi4 = sayi3;
        System.out.println("float değişken ile : " + sayi4);
        double sayi5 = sayi4;
        System.out.println("double değişken ile : " + sayi5);
        
		
		
	}

}
