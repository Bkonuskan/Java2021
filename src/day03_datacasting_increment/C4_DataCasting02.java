package day03_datacasting_increment;

public class C4_DataCasting02 {

	public static void main(String[] args) {

    double sayi1 = 3.14;
    
    int sayi2 = (int)sayi1;
    
    System.out.println(" double değişken : " + sayi1);
    System.out.println(" int değişken : " + sayi2);
    
    byte sayi3 = (byte) sayi2;
    
    System.out.println(" byte değişken : " + sayi3);

	}

}
