package day04_increment_matematik;

public class C1_IncrementDecrement {

	public static void main(String[] args) {
		
		int sayi1 = 10;
		int sayi2 = 20;
		
		sayi1-=5;
		sayi2+=10;
		
		System.out.println(sayi2 / sayi1);
		
		sayi1*=2;
		sayi2++;
		
		double sayi3 = sayi2/10;
		
		System.out.println(sayi3);
		System.out.println(sayi2 * sayi1);
        
	}

}
