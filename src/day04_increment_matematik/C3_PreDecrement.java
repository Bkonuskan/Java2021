package day04_increment_matematik;

public class C3_PreDecrement {

	public static void main(String[] args) {
		
		int sayi1=20;
		int sayi2=sayi1--;
		
		System.out.println("Pre-increment sayi1 : " + sayi1);//19
		System.out.println("Pre-increment sayi2 : " + sayi2);//20
		
		System.out.println(--sayi2);//19
		System.out.println(sayi2--);//19
		System.out.println(sayi2);//18
		
		
		
		
		

	}

}
